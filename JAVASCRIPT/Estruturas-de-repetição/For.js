// Estrutura de repetição
// for significa " para "
// break - para a execução do loop
// continue - pula a execução do momento
// for(inicialização de uma variável; condição de continuação para o loop; expressão final)


for(let i = 10; i > 0; i--) {
    if(i === 5) {
        continue;
    }

    console.log(i)
}